export function buildCatalogCategoryContent(input: CategoryContentInput) {
  const template = CATEGORY_TEMPLATES[input.branchSlug];
  if (!template) {
    return {
      lead: buildDefaultCategoryLead(input),
      seo: buildDefaultCategorySeo(input)
    };
  }

  return {
    lead: template.lead(input),
    seo: template.seo(input)
  };
}
